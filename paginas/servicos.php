                    <p class="mt-5">Em 2019, o número de milionários no Brasil chegou a 259 mil, o que representa um aumento de 19,35% em comparação com o ano anterior, quando eram 217 mil milionários. Os dados são do Global Wealth Report, publicação anual do Credit Suisse Research Institute sobre a riqueza em todo o mundo, e consideram os valores em dólares — ou seja, quem tem patrimônio acima de US$ 1 milhão.</p>
                    <p>A alta brasileira de milionários foi a sexta maior do mundo, atrás de Holanda, Alemanha, China, Japão e Estados Unidos.</p>
