                        <p class="mt-5">A previsão é de que, até 2024, o crescimento do número de milionários seja de 23%, chegando a 319 mil pessoas. A pesquisa estima que o 1% mais rico da população brasileira detém 49% de toda a riqueza familiar do País, que chega a US$ 3,5 trilhões.</p>
                        <p>O resultado é ainda mais impressionante no caso dos chamados ultra-ricos, que têm patrimônio acima de US$ 50 milhões: o Brasil teve a segunda maior alta global, atrás apenas dos Estados Unidos. Enquanto os EUA ganharam 4,2 mil ultra-ricos, o Brasil viu o número subir em 860 pessoas entre 2017 e 2018.</p>

                        <h3 class="mt-5 mb-4">Crescimento da riqueza mundial</h3>
                        <p>Já a riqueza global aumentou 2,6% e chegou a US$ 360 milhões — o que equivale a US$ 70.850 por adulto. Mesmo em meio às incertezas da guerra comercial, os países responsáveis pela alta foram principalmente os Estados Unidos, com crescimento de US$ 3,8 trilhões, e China, com US$ 1,9 trilhão. A Europa é a terceira maior responsável pelo crescimento na riqueza global, com US$ 1,1 trilhão do aumento.</p>
                        <p>A previsão é que riqueza global aumentará 27% até 2024, chegando a US$ 459 trilhões. Embora os países de renda baixa e média sejam responsáveis por apenas 31% da riqueza atual, a previsão é que eles responsam responsáveis por 38% do crescimento.</p>
                        <p>O 1% mais rico entre os milionários em todo o mundo concentra 44% de toda a riqueza global.</p>

                        <h3 class="mt-4 mb-4">Desigualdade no mundo</h3>
                        <p>O relatório aponta uma sutil diminuição da desigualdade de renda no mundo. Enquanto os 90% mais pobres do mundo representavam 11% da riqueza do mundo no 2000, hoje o porcentual é de 18%. O Credit Suisse ressalta ainda o alto índice de desigualdade registrado em 2016 e afirma que os números da época 'podem ter sido os mais altos para o futuro próximo'.</p>

                        <h3 class="mt-4 mb-4">Número de milionários no mundo</h3>
                        <p>O relatório estima que, atualmente, existam 46,8 milhões de milionários em todo o mundo. O número de 2019 representa um aumento de 1,1 milhão sobre 2018. O relatório aponta que, pela primeira vez, a China registrou mais membros na lista dos 10% mais ricos do mundo do que os Estados Unidos.</p>
                        <p>Enquanto na China são 100 milhões de pessoas, nos Estados Unidos são 99 milhões.</p>

